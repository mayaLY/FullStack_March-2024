//# Array Summation:
function sumArray(arr: number[]): number {
  return arr.reduce((sum, current) => sum + current, 0);
}
const numbers = [5, 10, 18, 25, 50];
console.log(`The sum of the array is: ${sumArray(numbers)}`);

